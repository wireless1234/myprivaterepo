#include <stdio.h>
/**
 *op_add - add numbers
 *@a: num1
 *@b: num2
 *Return: int
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 *op_sub - substract
 *@a: num1
 *@b: num2
 *Return: int
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 *op_mul - multiply
 *@a: num1
 *@b: num2
 *Return: int
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 *op_div - divide
 *@a: num1
 *@b: num2
 *Return: int
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 *op_mod - modulus
 *@a: num1
 *@b: num2
 *Return: int
 */
int op_mod(int a, int b)
{
	return (a % b);
}
